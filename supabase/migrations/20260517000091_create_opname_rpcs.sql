GRANT  EXECUTE ON FUNCTION create_opname_v1 TO authenticated;

COMMENT ON FUNCTION create_opname_v1 IS
  'Session 13 — Phase 2.D. MANAGER+ (inventory.opname.create). Opens a draft '
  'opname for a section. count_number autogenerated. Idempotent via p_idempotency_key.';

-- =============================================================
-- 2. add_opname_item_v1 — auto-loads expected_qty from section_stock
-- =============================================================

CREATE OR REPLACE FUNCTION add_opname_item_v1(
  p_count_id     UUID,
  p_product_id   UUID,
  p_expected_qty DECIMAL(10,3) DEFAULT NULL,
  p_notes        TEXT          DEFAULT NULL
) RETURNS JSONB
LANGUAGE plpgsql SECURITY DEFINER SET search_path = public
AS $$
DECLARE
  v_uid          UUID := auth.uid();
  v_profile      UUID;
  v_status       TEXT;
  v_section_id   UUID;
  v_product_unit TEXT;
  v_expected     DECIMAL(10,3);
  v_item_id      UUID;
BEGIN
  IF NOT has_permission(v_uid, 'inventory.opname.create') THEN
    RAISE EXCEPTION 'forbidden' USING ERRCODE='P0003';
  END IF;

  SELECT id INTO v_profile FROM user_profiles
    WHERE auth_user_id = v_uid AND deleted_at IS NULL;
  IF v_profile IS NULL THEN
    RAISE EXCEPTION 'forbidden' USING ERRCODE='P0003';
  END IF;

  SELECT status, section_id INTO v_status, v_section_id
    FROM inventory_counts WHERE id = p_count_id FOR UPDATE;
  IF NOT FOUND THEN
    RAISE EXCEPTION 'count_not_found' USING ERRCODE='P0002';
  END IF;

  IF v_status NOT IN ('draft','counting') THEN
    RAISE EXCEPTION 'add_item_not_allowed_in_status';
  END IF;

  SELECT unit INTO v_product_unit FROM products
    WHERE id = p_product_id AND deleted_at IS NULL;
  IF NOT FOUND THEN
    RAISE EXCEPTION 'product_not_found' USING ERRCODE='P0002';
  END IF;

  IF p_expected_qty IS NULL THEN
    SELECT COALESCE(quantity, 0) INTO v_expected
      FROM section_stock
      WHERE section_id = v_section_id AND product_id = p_product_id;
    IF v_expected IS NULL THEN
      v_expected := 0;
    END IF;
  ELSE
    v_expected := p_expected_qty;
  END IF;

  INSERT INTO inventory_count_items (
    count_id, product_id, expected_qty, unit, notes
  ) VALUES (
    p_count_id, p_product_id, v_expected, COALESCE(v_product_unit, 'pcs'), p_notes
  )
  ON CONFLICT (count_id, product_id) DO UPDATE
    SET expected_qty = EXCLUDED.expected_qty,
        unit         = EXCLUDED.unit,
        notes        = EXCLUDED.notes,
        updated_at   = now()
  RETURNING id INTO v_item_id;

  -- Flip status from draft to counting on first item.
  IF v_status = 'draft' THEN
    UPDATE inventory_counts SET status = 'counting' WHERE id = p_count_id;
  END IF;

  RETURN jsonb_build_object(
    'item_id',      v_item_id,
    'count_id',     p_count_id,
    'product_id',   p_product_id,
    'expected_qty', v_expected,
    'unit',         COALESCE(v_product_unit, 'pcs')
  );
END $$;

