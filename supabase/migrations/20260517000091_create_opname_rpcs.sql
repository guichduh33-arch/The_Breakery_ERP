-- 20260517000091_create_opname_rpcs.sql
-- Session 13 / Phase 2.D — Opname workflow RPCs.
--
-- Six high-level RPCs (SECURITY DEFINER, gated by has_permission) :
--   1. create_opname_v1   (MANAGER+ inventory.opname.create)
--   2. add_opname_item_v1 (MANAGER+ inventory.opname.create)
--   3. set_opname_count_v1 (MANAGER+ inventory.opname.create)
--   4. validate_opname_v1 (MANAGER+ inventory.opname.create) — draft|counting → review
--   5. finalize_opname_v1 (ADMIN+ inventory.opname.finalize)   — review|counting → finalized
--   6. cancel_opname_v1   (MANAGER+ inventory.opname.create)
--
-- finalize_opname_v1 emits ONE stock_movement per non-zero-variance row via
-- record_stock_movement_v1 (000020 v5). The companion trigger tr_20_je_emit
-- (000023) posts the JE :
--   variance > 0  → movement_type='opname_in'  → DR INVENTORY_GENERAL / CR OPNAME_INCOME
--   variance < 0  → movement_type='opname_out' → DR OPNAME_EXPENSE    / CR INVENTORY_GENERAL
--
-- IMPORTANT (Phase 2.D Deviation D-W2-2D-01) : the spec mentioned
-- 'opname_adjust_up' / 'opname_adjust_down' but tr_20_je_emit (Phase 1.A
-- migration 000022) handles 'opname_in' / 'opname_out' which were seeded in
-- the enum by migration 000014. We use the existing enum values to keep the
-- JE path live with no further enum extension.

-- =============================================================
-- 1. create_opname_v1
-- =============================================================

CREATE OR REPLACE FUNCTION create_opname_v1(
  p_section_id      UUID,
  p_notes           TEXT  DEFAULT NULL,
  p_idempotency_key UUID  DEFAULT NULL
) RETURNS JSONB
LANGUAGE plpgsql SECURITY DEFINER SET search_path = public
AS $$
DECLARE
  v_uid          UUID := auth.uid();
  v_profile      UUID;
  v_count_id     UUID;
  v_count_number TEXT;
  v_section_active BOOLEAN;
BEGIN
  IF NOT has_permission(v_uid, 'inventory.opname.create') THEN
    RAISE EXCEPTION 'forbidden' USING ERRCODE='P0003';
  END IF;

  IF p_idempotency_key IS NOT NULL THEN
    SELECT id, count_number INTO v_count_id, v_count_number
      FROM inventory_counts WHERE idempotency_key = p_idempotency_key;
    IF FOUND THEN
      RETURN jsonb_build_object(
        'count_id',          v_count_id,
        'count_number',      v_count_number,
        'idempotent_replay', true
      );
    END IF;
  END IF;

  SELECT id INTO v_profile FROM user_profiles
    WHERE auth_user_id = v_uid AND deleted_at IS NULL;
  IF v_profile IS NULL THEN
    RAISE EXCEPTION 'forbidden' USING ERRCODE='P0003';
  END IF;

  IF p_section_id IS NULL THEN
    RAISE EXCEPTION 'section_required';
  END IF;

  SELECT is_active INTO v_section_active
    FROM sections WHERE id = p_section_id AND deleted_at IS NULL;
  IF NOT FOUND OR v_section_active = false THEN
    RAISE EXCEPTION 'section_not_found' USING ERRCODE='P0002';
  END IF;

  v_count_number := next_count_number();

  INSERT INTO inventory_counts (
    count_number, section_id, status, created_by, notes, idempotency_key
  ) VALUES (
    v_count_number, p_section_id, 'draft', v_profile, p_notes, p_idempotency_key
  ) RETURNING id INTO v_count_id;

  INSERT INTO audit_log (action, subject_table, subject_id, payload, actor_profile_id)
  VALUES (
    'opname.create', 'inventory_counts', v_count_id,
    jsonb_build_object(
      'count_number',  v_count_number,
      'section_id',    p_section_id,
      'idempotency_key', p_idempotency_key
    ),
    v_profile
  );

  RETURN jsonb_build_object(
    'count_id',          v_count_id,
    'count_number',      v_count_number,
    'status',            'draft',
    'idempotent_replay', false
  );
END $$;

