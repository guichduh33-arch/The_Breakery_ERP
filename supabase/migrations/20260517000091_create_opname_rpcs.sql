-- =============================================================
-- 6. cancel_opname_v1 — non-finalized → cancelled
-- =============================================================

CREATE OR REPLACE FUNCTION cancel_opname_v1(
  p_count_id UUID,
  p_reason   TEXT
) RETURNS JSONB
LANGUAGE plpgsql SECURITY DEFINER SET search_path = public
AS $$
DECLARE
  v_uid          UUID := auth.uid();
  v_profile      UUID;
  v_status       TEXT;
  v_count_number TEXT;
BEGIN
  IF NOT has_permission(v_uid, 'inventory.opname.create') THEN
    RAISE EXCEPTION 'forbidden' USING ERRCODE='P0003';
  END IF;

  IF p_reason IS NULL OR length(trim(p_reason)) < 3 THEN
    RAISE EXCEPTION 'reason_required';
  END IF;

  SELECT id INTO v_profile FROM user_profiles
    WHERE auth_user_id = v_uid AND deleted_at IS NULL;
  IF v_profile IS NULL THEN
    RAISE EXCEPTION 'forbidden' USING ERRCODE='P0003';
  END IF;

  SELECT status, count_number INTO v_status, v_count_number
    FROM inventory_counts WHERE id = p_count_id FOR UPDATE;
  IF NOT FOUND THEN
    RAISE EXCEPTION 'count_not_found' USING ERRCODE='P0002';
  END IF;

  IF v_status = 'finalized' THEN
    RAISE EXCEPTION 'cancel_not_allowed_after_finalize';
  END IF;
  IF v_status = 'cancelled' THEN
    RAISE EXCEPTION 'already_cancelled';
  END IF;

  UPDATE inventory_counts
    SET status        = 'cancelled',
        cancelled_by  = v_profile,
        cancelled_at  = now(),
        cancel_reason = p_reason,
        metadata      = metadata || jsonb_build_object(
                                      'previous_status', v_status,
                                      'cancelled_at',    now()
                                    )
    WHERE id = p_count_id;

  INSERT INTO audit_log (action, subject_table, subject_id, payload, actor_profile_id)
  VALUES (
    'opname.cancel', 'inventory_counts', p_count_id,
    jsonb_build_object(
      'count_number',    v_count_number,
      'cancel_reason',   p_reason,
      'previous_status', v_status
    ),
    v_profile
  );

  RETURN jsonb_build_object(
    'count_id',      p_count_id,
    'status',        'cancelled',
    'cancel_reason', p_reason
  );
END $$;

REVOKE EXECUTE ON FUNCTION cancel_opname_v1 FROM PUBLIC;
GRANT  EXECUTE ON FUNCTION cancel_opname_v1 TO authenticated;

COMMENT ON FUNCTION cancel_opname_v1 IS
  'Session 13 — Phase 2.D. MANAGER+ (inventory.opname.create). Cancels a '
  'non-finalized opname with a reason (>=3 chars). Refused once status=finalized '
  'or already cancelled.';
