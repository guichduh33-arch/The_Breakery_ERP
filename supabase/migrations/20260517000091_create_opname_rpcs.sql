GRANT  EXECUTE ON FUNCTION add_opname_item_v1 TO authenticated;

COMMENT ON FUNCTION add_opname_item_v1 IS
  'Session 13 — Phase 2.D. MANAGER+ (inventory.opname.create). Adds (or upserts) '
  'a product row on an opname. When p_expected_qty is NULL, auto-loads from '
  'section_stock.quantity. First item flips count.status draft -> counting.';

