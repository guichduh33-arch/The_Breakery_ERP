-- =============================================================
-- 5. finalize_opname_v1 — emits adjustments + JE via trigger
-- =============================================================

CREATE OR REPLACE FUNCTION finalize_opname_v1(
  p_count_id        UUID,
  p_idempotency_key UUID DEFAULT NULL
) RETURNS JSONB
LANGUAGE plpgsql SECURITY DEFINER SET search_path = public
AS $$
DECLARE
  v_uid           UUID := auth.uid();
  v_profile       UUID;
  v_status        TEXT;
  v_section_id    UUID;
  v_count_number  TEXT;
  v_item          RECORD;
  v_movement      JSONB;
  v_movement_id   UUID;
  v_mvt_type      TEXT;
  v_qty           DECIMAL(10,3);
  v_movements     JSONB := '[]'::JSONB;
  v_emitted       INT := 0;
  v_existing_status TEXT;
BEGIN
  IF NOT has_permission(v_uid, 'inventory.opname.finalize') THEN
    RAISE EXCEPTION 'forbidden' USING ERRCODE='P0003';
  END IF;

  SELECT id INTO v_profile FROM user_profiles
    WHERE auth_user_id = v_uid AND deleted_at IS NULL;
  IF v_profile IS NULL THEN
    RAISE EXCEPTION 'forbidden' USING ERRCODE='P0003';
  END IF;

  SELECT status, section_id, count_number
    INTO v_status, v_section_id, v_count_number
    FROM inventory_counts WHERE id = p_count_id FOR UPDATE;
  IF NOT FOUND THEN
    RAISE EXCEPTION 'count_not_found' USING ERRCODE='P0002';
  END IF;

  -- Idempotent replay : if already finalized and key matches, return cached movements.
  IF v_status = 'finalized' THEN
    SELECT COALESCE(jsonb_agg(jsonb_build_object(
            'movement_id', sm.id,
            'product_id',  sm.product_id,
            'movement_type', sm.movement_type,
            'quantity',    sm.quantity,
            'unit',        sm.unit
          ) ORDER BY sm.created_at), '[]'::JSONB)
      INTO v_movements
      FROM stock_movements sm
      WHERE sm.reference_type = 'opname' AND sm.reference_id = p_count_id;
    RETURN jsonb_build_object(
      'count_id',          p_count_id,
      'count_number',      v_count_number,
      'status',            'finalized',
      'idempotent_replay', true,
      'movements_emitted', jsonb_array_length(v_movements),
      'movements',         v_movements
    );
  END IF;

  IF v_status NOT IN ('counting','review') THEN
    RAISE EXCEPTION 'finalize_not_allowed_in_status';
  END IF;

  -- Pre-check : no NULL counted_qty (in case finalize is called without validate).
  IF EXISTS (SELECT 1 FROM inventory_count_items
              WHERE count_id = p_count_id AND counted_qty IS NULL) THEN
    RAISE EXCEPTION 'missing_counts';
  END IF;

  -- Emit one movement per non-zero variance row.
  FOR v_item IN
    SELECT ici.id, ici.product_id, ici.variance, ici.unit, ici.expected_qty, ici.counted_qty
      FROM inventory_count_items ici
      WHERE ici.count_id = p_count_id AND COALESCE(ici.variance, 0) <> 0
  LOOP
    v_qty := ABS(v_item.variance);
    IF v_item.variance > 0 THEN
      v_mvt_type := 'opname_in';
      -- positive variance : section_stock GREW vs expected -> push +qty into section
      v_movement := record_stock_movement_v1(
        p_product_id      := v_item.product_id,
        p_movement_type   := 'opname_in'::movement_type,
        p_quantity        := v_qty,
        p_reason          := 'Opname ' || v_count_number || ' variance + ' || v_qty,
        p_unit_cost       := NULL,
        p_supplier_id     := NULL,
        p_idempotency_key := NULL,
        p_unit            := v_item.unit,
        p_from_section_id := NULL,
        p_to_section_id   := v_section_id,
        p_metadata        := jsonb_build_object(
                               'count_id',      p_count_id,
                               'count_number',  v_count_number,
                               'count_item_id', v_item.id,
                               'expected_qty',  v_item.expected_qty,
                               'counted_qty',   v_item.counted_qty
                             ),
        p_lot_id          := NULL
      );
    ELSE
      v_mvt_type := 'opname_out';
      v_movement := record_stock_movement_v1(
        p_product_id      := v_item.product_id,
        p_movement_type   := 'opname_out'::movement_type,
        p_quantity        := -v_qty,
        p_reason          := 'Opname ' || v_count_number || ' variance - ' || v_qty,
        p_unit_cost       := NULL,
        p_supplier_id     := NULL,
        p_idempotency_key := NULL,
        p_unit            := v_item.unit,
        p_from_section_id := v_section_id,
        p_to_section_id   := NULL,
        p_metadata        := jsonb_build_object(
                               'count_id',      p_count_id,
                               'count_number',  v_count_number,
                               'count_item_id', v_item.id,
                               'expected_qty',  v_item.expected_qty,
                               'counted_qty',   v_item.counted_qty
                             ),
        p_lot_id          := NULL
      );
    END IF;

    v_movement_id := (v_movement->>'movement_id')::UUID;
    UPDATE stock_movements
      SET reference_type = 'opname', reference_id = p_count_id
      WHERE id = v_movement_id;

    UPDATE inventory_count_items
      SET movement_id = v_movement_id, updated_at = now()
      WHERE id = v_item.id;

    v_movements := v_movements || jsonb_build_array(jsonb_build_object(
      'movement_id',   v_movement_id,
      'product_id',    v_item.product_id,
      'movement_type', v_mvt_type,
      'variance',      v_item.variance,
      'quantity',      v_qty,
      'unit',          v_item.unit
    ));
    v_emitted := v_emitted + 1;
  END LOOP;

  UPDATE inventory_counts
    SET status           = 'finalized',
        finalized_by     = v_profile,
        finalized_at     = now(),
        idempotency_key  = COALESCE(idempotency_key, p_idempotency_key),
        metadata         = metadata || jsonb_build_object(
                                         'movements_emitted', v_emitted,
                                         'finalized_at',      now()
                                       )
    WHERE id = p_count_id;

  INSERT INTO audit_log (action, subject_table, subject_id, payload, actor_profile_id)
  VALUES (
    'opname.finalize', 'inventory_counts', p_count_id,
    jsonb_build_object(
      'count_number',      v_count_number,
      'section_id',        v_section_id,
      'movements_emitted', v_emitted,
      'idempotency_key',   p_idempotency_key
    ),
    v_profile
  );

  RETURN jsonb_build_object(
    'count_id',          p_count_id,
    'count_number',      v_count_number,
    'status',            'finalized',
    'idempotent_replay', false,
    'movements_emitted', v_emitted,
    'movements',         v_movements
  );
END $$;

REVOKE EXECUTE ON FUNCTION finalize_opname_v1 FROM PUBLIC;
GRANT  EXECUTE ON FUNCTION finalize_opname_v1 TO authenticated;

COMMENT ON FUNCTION finalize_opname_v1 IS
  'Session 13 — Phase 2.D. ADMIN+ (inventory.opname.finalize). Transitions '
  'counting|review opname to finalized. Emits one stock_movement per non-zero '
  'variance row (opname_in for variance>0, opname_out for variance<0). The JE '
