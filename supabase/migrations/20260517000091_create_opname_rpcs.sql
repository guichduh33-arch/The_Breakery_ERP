-- =============================================================

CREATE OR REPLACE FUNCTION validate_opname_v1(
  p_count_id UUID
) RETURNS JSONB
LANGUAGE plpgsql SECURITY DEFINER SET search_path = public
AS $$
DECLARE
  v_uid     UUID := auth.uid();
  v_profile UUID;
  v_status  TEXT;
  v_missing INT;
BEGIN
  IF NOT has_permission(v_uid, 'inventory.opname.create') THEN
    RAISE EXCEPTION 'forbidden' USING ERRCODE='P0003';
  END IF;

  SELECT id INTO v_profile FROM user_profiles
    WHERE auth_user_id = v_uid AND deleted_at IS NULL;
  IF v_profile IS NULL THEN
    RAISE EXCEPTION 'forbidden' USING ERRCODE='P0003';
  END IF;

  SELECT status INTO v_status FROM inventory_counts
    WHERE id = p_count_id FOR UPDATE;
  IF NOT FOUND THEN
    RAISE EXCEPTION 'count_not_found' USING ERRCODE='P0002';
  END IF;

  IF v_status NOT IN ('draft','counting') THEN
    RAISE EXCEPTION 'validate_not_allowed_in_status';
  END IF;

  SELECT COUNT(*) INTO v_missing FROM inventory_count_items
    WHERE count_id = p_count_id AND counted_qty IS NULL;
  IF v_missing > 0 THEN
    RAISE EXCEPTION 'missing_counts' USING DETAIL=format('%s row(s) missing counted_qty', v_missing);
  END IF;

  UPDATE inventory_counts
    SET status = 'review'
    WHERE id = p_count_id;

  RETURN jsonb_build_object(
    'count_id', p_count_id,
    'status',   'review'
  );
END $$;

